export function calcularTendencia(
  actual: number,
  anterior: number | null
): Tendencia {
  if (!anterior) return 'estable'
  const diff = actual - anterior
  if (diff > 0.05) return 'subida'
  if (diff < -0.05) return 'bajada'
  return 'estable'
}

export function esNotaAlta(nota: number): boolean {
  return nota >= 12
}

export function esMuyDemandado(grado: NotaDeCorte): boolean {
  return grado.solicitudes / grado.plazas > 5
}
